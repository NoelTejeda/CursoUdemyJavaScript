/**
 * Tipos de datos
 */

//números -number
console.log("40");
console.log("40,44");

//letras -string
console.log("Noel Tejeda");
console.log("Noel Tejeda");
console.log(`Noel Tejeda`); // comillas invertidas alt+96

//lógicos ó bolleans
console.log(true);
console.log(false);

//indefinidos - undefined
console.log(undefined);

//null
console.log(null);

/**
 * tipos de datos estructurales
 */

//tipo de dato objeto -object
console.log({
  nombre: "Noel Tejeda",
  edad: 40,
  fechaDeNacimiento: null,
  esAlto: false,
  ciudades: ["Quito", "Guayaquil"],
});

//tipo de dato arreglo (lista)
console.log(["Venezuela", "Colombia", "Ecuador", "Perú"]);
//lista con datos variados
console.log([21, true, { nombre: "Noel Tejeda" }, [1, 2, 3, 4, 5]]);
